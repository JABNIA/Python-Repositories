from isVowel import is_vowel

def test_vowel():
    assert is_vowel("a") == ""
    assert is_vowel("e") == ""
    assert is_vowel("i") == ""
    assert is_vowel("o") == ""
    assert is_vowel("u") == ""
    assert is_vowel("A") == ""
    assert is_vowel("E") == ""
    assert is_vowel("U") == ""
    assert is_vowel("I") == ""
    assert is_vowel("O") == ""
